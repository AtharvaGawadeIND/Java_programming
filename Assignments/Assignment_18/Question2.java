////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : getEvenNumbers
//  Description     : It returns a string containing all even numbers from 1 up to N (inclusive).
//  Input           : Integer (n)
//  Output          : String (Space-separated even numbers)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    String getEvenNumbers(int n)
    {
        String result = "";
        for(int i = 1; i <= n; i++)
        {
            if(i % 2 == 0)
                result += i + " ";
        }
        return result.trim(); // Use trim to remove trailing space
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question2 {
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int n = 20;
        System.out.println("Even numbers up to " + n + ": " + obj.getEvenNumbers(n));
    }
    
}
