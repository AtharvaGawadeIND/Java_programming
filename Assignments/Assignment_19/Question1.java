////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : checkLeapYear
//  Description     : Checks whether a given year is a leap year or not.
//  Input           : Integer (year)
//  Output          : Displays whether the year is leap or not.
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
