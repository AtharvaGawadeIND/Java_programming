package Assignment_20;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : sumEvenNumbers
//  Description     : It is used to calculate the sum of all even numbers up to N (inclusive).
//  Input           : Integer (N)
//  Output          : Integer (Sum)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int sumEvenNumbers(int n)
    {
        int sum = 0;
        for(int i = 2; i <= n; i += 2)
            sum += i;
        return sum;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question1
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int n = 10;
        int ans = obj.sumEvenNumbers(n);
        System.out.println("Sum of even numbers up to " + n + " = " + ans);
    }
}
