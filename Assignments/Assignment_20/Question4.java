package Assignment20;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : findLargestDigit
//  Description     : It is used to find the largest digit present in a given number.
//  Input           : Integer (num)
//  Output          : Integer (max)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int findLargestDigit(int num)
    {
        int max = 0;
        while(num != 0)
        {
            int digit = num % 10;
            if(digit > max)
                max = digit;
            num /= 10;
        }
        return max;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question4
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int num = 83429;
        int ans = obj.findLargestDigit(num);
        System.out.println("Largest digit = " + ans);
    }
}
