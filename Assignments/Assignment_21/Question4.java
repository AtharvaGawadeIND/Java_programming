//  Function Name   : countFactors
//  Description     : It is used to count the total number of factors of a given number
//  Input           : Integer
//  Output          : Integer
//  Author          : Atharva Vinod Gawade
//  Date            : 01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int countFactors(int num)
    {
        int count = 0;
        for(int i = 1; i <= num; i++)
        {
            if(num % i == 0)
                count++;
        }
        return count;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question4
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int num = 20;
        int ans = obj.countFactors(num);
        System.out.println("Total number of factors = " + ans);
    }
}
