package Assignment21;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : productOfDigits
//  Description     : It is used to calculate the product of digits of a given number
//  Input           : Integer
//  Output          : Integer
//  Author          : Atharva Vinod Gawade
//  Date            : 01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int productOfDigits(int num)
    {
        int product = 1;
        while(num != 0)
        {
            product *= num % 10;
            num /= 10;
        }
        return product;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question1
{
    public static void main(String[] args)
    {
        Logic obj = new Logic();
        int num = 234;
        int ans = obj.productOfDigits(num);
        System.out.println("Product of digits = " + ans);
    }
}
