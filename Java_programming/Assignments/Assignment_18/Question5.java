////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : checkSign
//  Description     : It checks whether a number is positive, negative, or zero.
//  Input           : Integer (num)
//  Output          : Void (prints result to console)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void checkSign(int num)
    {
        if(num > 0)
            System.out.println(num + " is Positive");
        else if(num < 0)
            System.out.println(num + " is Negative");
        else
            System.out.println(num + " is Zero");
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question5 {

   public static void main(String args[])
    {
        Logic obj = new Logic();
        
        System.out.print("Input: 5 -> Output: ");
        obj.checkSign(5);
        
        System.out.print("Input: -8 -> Output: ");
        obj.checkSign(-8);
        
        System.out.print("Input: 0 -> Output: ");
        obj.checkSign(0);
    }
    
}
