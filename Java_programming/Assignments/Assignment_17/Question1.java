////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : sumOfDigits
//  Description     : It is used to calculate the sum of all digits of an input number.
//  Input           : Integer (inum)
//  Output          : void (prints the result)
//  Author          : Atharva Vinod Gawade
//  Date            : 29/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////
class Logic
{
    void sumOfDigits(int inum)
    {
        int isum = 0;
        while(inum != 0)
        {
            int digit = inum % 10;
            isum = isum + digit;
            inum = inum / 10;
        }
        System.out.println("Sum of digits are " + isum);
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question1 {

    public static void main(String args[])
    {
        Logic obj = new Logic();
        obj.sumOfDigits(1234);
    }
    
}
