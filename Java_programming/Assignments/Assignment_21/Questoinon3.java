class Logic
{
    void displayFactors(int num)
    {
        System.out.print("Factors of " + num + " are: ");
        
        for(int i = 1; i <= num; i++)
        {
            if(num % i == 0)
            {
                System.out.print(i + " ");
            }
        }
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Questoinon3
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int num = 12;
        obj.displayFactors(num);
    }
}
