package Assignment_20;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : printReverse
//  Description     : It is used to print numbers from N down to 1 in reverse order.
//  Input           : Integer (N)
//  Output          : Void (prints to console)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printReverse(int n)
    {
        for(int i = n; i >= 1; i--)
            System.out.print(i + " ");
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question2
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        System.out.print("Numbers in reverse: ");
        obj.printReverse(10);
        System.out.println(); // For a newline at the end of the output
    }
}
