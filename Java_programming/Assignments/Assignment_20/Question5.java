package Assignment_20;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : findSmallestDigit
//  Description     : It is used to find the smallest digit present in a given number.
//  Input           : Integer (num)
//  Output          : Integer (Smallest digit)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int findSmallestDigit(int num)
    {
        int min = 9;
        while(num != 0)
        {
            int digit = num % 10;
            if(digit < min)
                min = digit;
            num /= 10;
        }
        return min;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question5
{
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int num = 45872;
        int ans = obj.findSmallestDigit(num);
        System.out.println("Smallest digit = " + ans);
    }
}
