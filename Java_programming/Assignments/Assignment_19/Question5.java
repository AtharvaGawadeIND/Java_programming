package Assignment_19;
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : calculatePower
//  Description     : It is used to calculate the power of a base number raised to an exponent.
//  Input           : Integer (base), Integer (exp)
//  Output          : Integer (result)
//  Author          : Atharva Vinod Gawade
//  Date            : 30/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    int calculatePower(int base, int exp)
    {
        int result = 1;
        for(int i = 1; i <= exp; i++)
            result *= base;
        return result;
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////
public class Question5 {
    
    public static void main(String args[])
    {
        Logic obj = new Logic();
        int base = 2, exp = 5;
        int ans = obj.calculatePower(base, exp);
        System.out.println(base + " raised to " + exp + " = " + ans);
    }

}
